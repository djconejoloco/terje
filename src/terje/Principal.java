    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        //creamos objeto file chooser
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.laz " , "LAZ" ,"*.las", "LAS");
        fc.setFileFilter(filtro);
                
        //abrimos la ventana
        int seleccion = fc.showOpenDialog(contentPane);
        if(seleccion==JFileChooser.APPROVE_OPTION){
            //seleccionamos fichero con la clase file
            File fich =fc.getSelectedFile();
            jTextField1.setText(fich.getAbsolutePath());
            jTextField1.setToolTipText(null);
            
            try(FileReader fr = new FileReader(fich)){
                String cadena="";
                int valor=fr.read();
                while(valor!=-1){
                    cadena=cadena+(char)valor;
                    valor=fr.read();
                    
                }
                
                
            }catch (IOException e1){
                e1.printStackTrace();
                
            }
            
        }
        
        
    }//GEN-LAST:event_button1ActionPerformed

